export function getTextTypeGuidance(textType) {
  const entry = TEXT_TYPE_CATALOG[textType];
  if (!entry) return "";
  return [
    `Textsorte "${textType}" - Erwartungshorizont:`,
    `- Inhaltlicher Fokus: ${entry.focus}`,
    `- Aufbau: ${entry.structure}`,
    `- Stil/Wirkung: ${entry.style}`,
  ].join("\n");
}
